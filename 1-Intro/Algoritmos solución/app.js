//"Console log" es una foto del momento
console.log(suma); 

let resta = 10 - 5;
console.log(resta);


let multiplicación = 5 * 3;
console.log(multiplicación);


let división = 10 / 3;
console.log(división);
console.log(división.toFixed(2));
